#include "main.h"

/**
 * main - check the code.
 * Discription: function that prints the sign of a number
 * Return: Always Zero
 */

int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n == 0)
	{
		_putchar('0');
		return (0);
	}
	else
	{
		_putchar('-');
		return (-1);
	}
}
